#include <cstdio>
#include <cstdlib>
#include <sys/socket.h>
#include <netinet/in.h>
#include <arpa/inet.h>
#include <cstring>
#include <errno.h>
#include <unistd.h>

#define SERVER_PORT 50000
#define SERVER_ADDR "localhost"

int main(int argc, char *argv[]) {
    int server_socket;
    int peer_socket;
    socklen_t sock_len;
    ssize_t len;
    struct sockaddr_in server_addr;
    struct sockaddr_in peer_addr;
    int file_size;
    int remain_data;
    char buffer[BUFSIZ];
    char filename[255];

    server_socket = socket(AF_INET, SOCK_STREAM, 0);
    if (server_socket == -1) {
        fprintf(stderr, "Error constructing socket: %s", strerror(errno));

        exit(EXIT_FAILURE);
    }

    /* Zeroing server_addr struct */
    memset(&server_addr, 0, sizeof(server_addr));
    /* Construct server_addr struct */
    server_addr.sin_family = AF_INET;
    inet_pton(AF_INET, SERVER_ADDR, &(server_addr.sin_addr));
    server_addr.sin_port = htons(SERVER_PORT);

    /* Bind */
    if ((bind(server_socket, (struct sockaddr *) &server_addr, sizeof(struct sockaddr))) == -1) {
        fprintf(stderr, "Error on bind: %s", strerror(errno));
        return EXIT_FAILURE;
    }

    /* Listening to incoming connections */
    if ((listen(server_socket, 5)) == -1) {
        fprintf(stderr, "Error on listen: %s", strerror(errno));
        return EXIT_FAILURE;
    }

    sock_len = sizeof(struct sockaddr_in);
    /* Accepting incoming peers */
    peer_socket = accept(server_socket, (struct sockaddr *) &peer_addr, &sock_len);
    if (peer_socket == -1) {
        fprintf(stderr, "Error on accept: %s", strerror(errno));
        return EXIT_FAILURE;
    }
    recv(peer_socket, buffer, BUFSIZ, 0);
    strcpy(filename, buffer);

    recv(peer_socket, buffer, BUFSIZ, 0);
    file_size = atoi(buffer);

    fprintf(stdout, "Filename: \"%s\", Filesize: \"%d\"\n", filename, file_size);

    FILE *received_file = fopen(filename, "wb");
    if (received_file == NULL) {
        fprintf(stderr, "Can't create file \"%s\" with error \"%s\"", filename, strerror(errno));
        return EXIT_FAILURE;
    }

    remain_data = file_size;
    while (((len = recv(peer_socket, buffer, BUFSIZ, 0)) > 0) && (remain_data > 0)) {
        fwrite(buffer, sizeof(char), len, received_file);
        remain_data -= len;
    }
    fclose(received_file);
    close(peer_socket);
    close(server_socket);

    return 0;
}